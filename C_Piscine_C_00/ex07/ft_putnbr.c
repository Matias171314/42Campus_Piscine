#include <unistd.h>

void	ft_puntbr(int nb)
{
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb == -2147483648)
	{
		write(1, "2147483648", 12);
		return ;
	}

}
